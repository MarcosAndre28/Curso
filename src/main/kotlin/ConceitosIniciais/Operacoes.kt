package ConceitosIniciais

fun main() {
    println("2 + 2 = ${2 + 2}")
    println("2 - 2 = ${2 - 2}")
    println("2 / 2 = ${2 / 2}")
    println("2 * 2 = ${2 * 2}")


    var numero = 10

    println("numero++ = ${numero++}")
    println("numero-- = ${numero--}")

    println("++numero = ${++numero}")
    println("--numero = ${--numero}")

    numero += 2
    println("numero+= 2 = $numero")

    numero -= 2
    println("numero-= 2 = $numero")

    numero /= 2
    println("numero/= 2 = $numero")

    numero *= 2
    println("numero*= 2 = $numero")

    numero -= 2
    println("numero-= 2 = $numero")






}